void write_byte(unsigned char data) {
    char i;
    for (i = 8; i > 0; i--) {
        CLK_0;
        if (0x80 & data) {
            DIN_1;
        } else {
            DIN_0;
        }
        data = data << 1;
        CLK_1;
    }
}


void write(unsigned char addr1, unsigned char data1, unsigned char addr2, unsigned char data2)
{
     CS_0;
     write_byte(addr1);              //
     write_byte(data1);              //
     write_byte(addr2);              //
     write_byte(data2);              //
     CS_1;
}

void init(void)
{
    P6DIR |= BIT0 + BIT1 + BIT2;
    write(0x09, 0x00, 0x09, 0x00);       //BCD
    write(0x0A, 0x03, 0x0A, 0x03);       //
    write(0x0B, 0x07, 0x0B, 0x07);       //8
    write(0x0C, 0x01, 0x0C, 0x01);       //01
    write(0x0F, 0x01, 0x0F, 0x01);       //10
    delay_ms(1000);
    write(0x0F, 0x00, 0x0F, 0x00);
}

unsigned int char2bit(char m) {
    unsigned int n;
    if (m == '\0') {
        return 99;
    }
    if (65 <= m && m <= 90) {
        n = m - 64;
    } else if (97 <= m && m <= 122) {
        n = m - 96 + 26;
    } else if (48 <= m && m <= 64) {
        n =  m - 47 + 26 * 2;
        // :;<=>?@
        // :,.()?-
    } else {
        n = 0;
    }
    return n;
}



